package vending_machine;

class User {
    String role;
    String email;

    void setRole(String role) {
        this.role = role;
    }

    String getRole() {
        return this.role;
    }

    void setEmail(String email) {
        this.email = email;
    }

    String getEmail() {
        return this.email;
    }
}
